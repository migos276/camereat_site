// Fonction pour changer la langue
function changeLanguage(lang) {
  // Sauvegarder la langue choisie
  localStorage.setItem('preferredLanguage', lang);

  // Mettre √† jour tous les √©l√©ments avec data-translate
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang][key]) {
      element.innerHTML = translations[lang][key];
    }
  });

  // Mettre √† jour l'indicateur de langue
  const langIndicator = document.querySelector('.language-selector span:last-child');
  if (langIndicator) {
    langIndicator.textContent = lang.toUpperCase();
  }

  // Ajouter la classe active au bouton de langue s√©lectionn√©
  document.querySelectorAll('.lang-option').forEach(option => {
    option.classList.remove('active');
  });
  document.querySelector(`[data-lang="${lang}"]`)?.classList.add('active');
}

